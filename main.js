function init() {
  const container = document.getElementById("markets-container");
  const filterContainer = document.getElementById("filter-container");
  const themeToggle = document.getElementById("theme-toggle");
  const langButtons = document.querySelectorAll(".lang-btn");
  
  let currentLang = localStorage.getItem("lang") || "ko";
  let currentDistrictKey = "ALL";

  themeToggle.addEventListener("click", () => {
    document.documentElement.classList.toggle("light-mode");
    const isLight = document.documentElement.classList.contains("light-mode");
    themeToggle.textContent = isLight ? "🌙" : "🌓";
    localStorage.setItem("theme", isLight ? "light" : "dark");
  });

  if (localStorage.getItem("theme") === "light") {
    document.documentElement.classList.add("light-mode");
    themeToggle.textContent = "🌙";
  }

  function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem("lang", lang);
    
    const t = translations[lang];
    const mainTitle = document.getElementById("main-title");
    if (mainTitle) mainTitle.textContent = t.title;
    
    const mainSubtitle = document.getElementById("main-subtitle");
    if (mainSubtitle) mainSubtitle.textContent = t.subtitle;
    
    const footerText = document.getElementById("footer-text");
    if (footerText) footerText.innerHTML = t.footer;

    const navHome = document.getElementById("nav-home");
    if (navHome) navHome.textContent = t.navHome;
    const navGame = document.getElementById("nav-game");
    if (navGame) navGame.textContent = t.navGame;
    const navTransform = document.getElementById("nav-transform");
    if (navTransform) navTransform.textContent = t.navTransform;
    const navAbout = document.getElementById("nav-about");
    if (navAbout) navAbout.textContent = t.navAbout;
    const navContact = document.getElementById("nav-contact");
    if (navContact) navContact.textContent = t.navContact;
    const navPrivacy = document.getElementById("nav-privacy");
    if (navPrivacy) navPrivacy.textContent = t.navPrivacy;

    const guideTitle = document.getElementById("guide-title");
    if (guideTitle) guideTitle.textContent = t.guideTitle;
    const tip1Title = document.getElementById("guide-tip1-title");
    if (tip1Title) tip1Title.textContent = t.guideTip1Title;
    const tip1Desc = document.getElementById("guide-tip1-desc");
    if (tip1Desc) tip1Desc.textContent = t.guideTip1Desc;
    const tip2Title = document.getElementById("guide-tip2-title");
    if (tip2Title) tip2Title.textContent = t.guideTip2Title;
    const tip2Desc = document.getElementById("guide-tip2-desc");
    if (tip2Desc) tip2Desc.textContent = t.guideTip2Desc;
    const tip3Title = document.getElementById("guide-tip3-title");
    if (tip3Title) tip3Title.textContent = t.guideTip3Title;
    const tip3Desc = document.getElementById("guide-tip3-desc");
    if (tip3Desc) tip3Desc.textContent = t.guideTip3Desc;

    const footerAbout = document.getElementById("footer-about");
    if (footerAbout) footerAbout.textContent = t.navAbout;
    const footerContact = document.getElementById("footer-contact");
    if (footerContact) footerContact.textContent = t.navContact;
    const footerPrivacy = document.getElementById("footer-privacy");
    if (footerPrivacy) footerPrivacy.textContent = t.navPrivacy;

    langButtons.forEach(btn => {
      btn.classList.toggle("active", btn.dataset.lang === lang);
    });

    if (filterContainer && container) {
      renderFilters();
      renderMarkets();
    }
  }

  langButtons.forEach(btn => {
    btn.addEventListener("click", () => setLanguage(btn.dataset.lang));
  });

  function renderFilters() {
    filterContainer.innerHTML = "";
    const t = translations[currentLang];
    
    const currentDistrictList = [
      { key: "ALL", label: t.all },
      { key: "종로구", label: t.districts["종로구"] || "종로구" },
      { key: "중구", label: t.districts["중구"] || "중구" },
      { key: "성동구", label: t.districts["성동구"] || "성동구" },
      { key: "동작구", label: t.districts["동작구"] || "동작구" },
      { key: "마포구", label: t.districts["마포구"] || "마포구" },
      { key: "동대문구", label: t.districts["동대문구"] || "동대문구" },
      { key: "강북구", label: t.districts["강북구"] || "강북구" },
      { key: "영등포구", label: t.districts["영등포구"] || "영등포구" },
      { key: "송파구", label: t.districts["송파구"] || "송파구" },
      { key: "강남구", label: t.districts["강남구"] || "강남구" },
      { key: "광진구", label: t.districts["광진구"] || "광진구" },
      { key: "강동구", label: t.districts["강동구"] || "강동구" },
      { key: "FULL_LIST", label: t.fullList }
    ];
    
    currentDistrictList.forEach(d => {
      const btn = document.createElement("button");
      btn.className = `filter-btn ${currentDistrictKey === d.key ? "active" : ""}`;
      btn.textContent = d.label;
      btn.addEventListener("click", () => {
        currentDistrictKey = d.key;
        renderFilters();
        renderMarkets();
      });
      filterContainer.appendChild(btn);
    });
  }

  function renderMarkets() {
    container.innerHTML = "";
    const t = translations[currentLang];
    
    if (currentDistrictKey === "FULL_LIST") {
      const card = document.createElement("market-card");
      card.setAttribute("name", t.miscMarketName);
      card.setAttribute("district", t.other);
      card.setAttribute("description", t.miscMarketDesc);
      card.setAttribute("food", t.miscMarketFood);
      card.setAttribute("sights", t.miscMarketSights);
      card.setAttribute("map-url", "https://ko.wikipedia.org/wiki/%EC%84%9C%EC%9A%B8%ED%8A%B9%EB%B3%84%EC%8B%9C%EC%9D%98_%EC%8B%9C%EC%9E%A5_%EB%AA%A9%EB%A1%9D");
      
      card.setAttribute("lang-food", t.food);
      card.setAttribute("lang-sights", t.sights);
      card.setAttribute("lang-visit", t.visitOfficial);
      card.setAttribute("lang-viewlist", t.viewAllList);
      
      container.appendChild(card);
      return;
    }

    let filtered = marketsData;
    if (currentDistrictKey !== "ALL") {
      filtered = marketsData.filter(m => m.district === currentDistrictKey);
    }

    filtered.forEach(market => {
      const card = document.createElement("market-card");
      const m = market.translations[currentLang];
      const translatedDistrict = t.districts[market.district] || market.district;

      card.setAttribute("name", m.name);
      card.setAttribute("image", market.image);
      card.setAttribute("district", translatedDistrict);
      card.setAttribute("description", m.description);
      card.setAttribute("food", m.food);
      card.setAttribute("sights", m.sights);
      card.setAttribute("map-url", market.mapUrl);
      card.setAttribute("official-url", market.officialUrl || "");
      card.setAttribute("lang-food", t.food);
      card.setAttribute("lang-sights", t.sights);
      card.setAttribute("lang-visit", t.visitOfficial);
      card.setAttribute("lang-viewlist", t.viewAllList);
      
      container.appendChild(card);
    });

    if (currentDistrictKey === "ALL" || currentDistrictKey === "OTHER") {
      const card = document.createElement("market-card");
      card.setAttribute("name", t.miscMarketName);
      card.setAttribute("district", t.other);
      card.setAttribute("description", t.miscMarketDesc);
      card.setAttribute("food", t.miscMarketFood);
      card.setAttribute("sights", t.miscMarketSights);
      card.setAttribute("map-url", "https://ko.wikipedia.org/wiki/%EC%84%9C%EC%9A%B8%ED%8A%B9%EB%B3%84%EC%8B%9C%EC%9D%98_%EC%8B%9C%EC%9E%A5_%EB%AA%A9%EB%A1%9D");
      
      card.setAttribute("lang-food", t.food);
      card.setAttribute("lang-sights", t.sights);
      card.setAttribute("lang-visit", t.visitOfficial);
      card.setAttribute("lang-viewlist", t.viewAllList);
      
      container.appendChild(card);
    }
  }

  setLanguage(currentLang);
}

document.addEventListener("DOMContentLoaded", init);
